// Paso 1 solicitar datos la usuario
var numero_1 = parseInt(prompt("Ingresa el primer numero 🤓: "));
var numero_2 = parseInt(prompt("Ingresa el segundo numero 🤓: "));
var numero_3 = parseInt(prompt("Ingresa el tercer numero 🤓: "));
var numero_4 = parseInt(prompt("Ingresa el cuarto numero 🤓: "));
var numero_5 = parseInt(prompt("Ingresa el quinto numero 🤓: "));

// Paso 2 realizar operación
var suma = numero_1 + numero_2 + numero_3 + numero_4 + numero_5;
var promedio = suma / 5;

// Paso 3 mostrar resultado
alert(
  `la suma de los numeros es 🥁🥁🥁 ${suma}, y su promedio es... ${promedio} 😱`
);
